class SuburbPerfData:

	def __init__(self, suburb_id, suburb_name, month, year, lowest_sold_price, highest_sold_price, median_sold_price):
		self.suburb_id = suburb_id
		self.suburb_name = suburb_name
		self.month = month
		self.year = year
		self.lowest_sold_price = lowest_sold_price
		self.highest_sold_price = highest_sold_price
		self.median_sold_price = median_sold_price